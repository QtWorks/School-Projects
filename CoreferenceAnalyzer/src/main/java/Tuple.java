public class Tuple<X,Y,Z,A,B> {
    public final X x;
    public final Y y;
    public final Z z;
    public final A a;
    public final B b;
    public Tuple(X x, Y y, Z z, A a, B b){
        this.x = x;
        this.y = y;
        this.z = z;
        this.a = a;
        this.b = b;
    }
}
